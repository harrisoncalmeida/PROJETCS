# Solicitar ao ususário que insira a temperatura em graus Celsius
celsius = float(input("Por favor, insira a temperatura em graus Celsius: "))

# Calcular a temperatura em graus Fahrenheit usando a fórmula
fahrenheit = (celsius * 9/5) + 32

# Exibe as temperaturas
print(f"{celsius} graus Celsius é igual a {fahrenheit} graus Fahrenheit.")